/////////////////////////////////////////////////////////
//Importaciones
////////////////////////////////////////////////////////
const {Schema, model} = require('mongoose');
/////////////////////////////////////////////////////////

/////////////////////////////////////////////////////////
//Esquema de los roles
////////////////////////////////////////////////////////
const RoleSchema = Schema({

    rol:{
        type: String,
        required: [true,'El rol es obligatorio']
    }
});
////////////////////////////////////////////////////////

//Exportacion del modelo
module.exports = model('Role', RoleSchema);